package org.sunspotworld;

/**
 *
 * @author Muhammed
 */
public class Rectangle
{
  short x1;
  short x2;
  short y1;
  short y2;
  short weight;

  public Rectangle(short x1, short y1, short x2, short y2, short weight)
  {
    this.x1 = x1;
    this.x2 = x2;
    this.y1 = y1;
    this.y2 = y2;
    this.weight = weight;
  }
}
